from flask_script import Manager, Shell
from app import app
import os


def make_shell_context():
    return dict(app=app)


manager = Manager(app)

manager.add_command("shell", Shell(make_context=make_shell_context))

root_path = os.getcwd()
static_path = os.path.join(root_path,'app')

if __name__ == "__main__":
    manager.run()
